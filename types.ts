// Этот файл определяет TypeScript типы, которые используются в разных частях приложения.

// Интерфейс Message описывает структуру одного сообщения в чате.
export interface Message {
  // 'role' указывает, кто отправил сообщение.
  // Это может быть либо 'user' (пользователь), либо 'model' (AI-модель).
  role: 'user' | 'model';
  // 'text' содержит текстовое содержимое сообщения.
  text: string;
}
